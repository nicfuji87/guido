interface SignupResult {
  success: boolean;
  data?: {
    conta_id: string;
    corretor_id: string;
    usuario_id: string;
    assinatura_id: string;
    auth_user_id?: string;
  };
  error?: string;
}

// AI dev note: DadosAsaas removido - agora processamento via webhook n8n
